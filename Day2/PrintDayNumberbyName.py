DayName = input("Day Name is:")


if DayName.upper()=="SUNDAY":
    print("Day No is : 1")
elif DayName.upper()=="MONDAY":
    print("Day No is : 2")
elif DayName.upper()=="TUESDAY":
    print("Day No is : 3")
elif DayName.upper()=="WEDNESDAY":
    print("Day No is : 4")
elif DayName.upper()=="THURSDAY":
    print("Day No is : 5")
elif DayName.upper()=="FRIDAY":
    print("Day No is : 6")
elif DayName.upper()=="SATURDAY":
    print("Day No is : 7")
else:
    print("INVALID DAY NAME PROVIDED")
