#For loop

#print  1.....10 using for loop
for i in range(10):
    print(i)
print("------------------------------")
#print  exact 1.....10 using for loop
for i in range(1,11):
    print(i)
print("------------------------------")
#print only even numbers 1 to 20

for i in range(2,21,2):
    print(i)
print("------------------------------")
#print only Odd numbers 1 to 20

for i in range(1,20,2):
    print(i)
print("------------------------------")

#print  numbers 1 to 20 in descending order

for i in range(10,0,-1):
    print(i)
print("------------------------------")

#print  numbers 5 to 100 in incrementing by 5
for i in range(5,105,5):
    print(i)
print("------------------------------")
